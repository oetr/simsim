	mov H4, ST24
	ldi ZH, hi8(sbox02)
	lpm H3, Z
	eor ST34, H3
	mov ZL, ST43
	ldd ST43, Y+11
	eor ST43, H1
	lpm H5, Z
	eor H3, H5
	eor H4, H5
	ldi ZH, hi8(sbox)
	lpm H5, Z
	eor ST24, H5
	eor ST34, H5
	eor H3, H5
	mov ZL, ST14
	lpm H5, Z
	eor ST34, H5
	eor H3, H5
